const FALLBACK_THEME = {
    id: 'general',
    label: 'Ostatní',
};

function normalizeText(value) {
    return String(value || '')
        .toLowerCase()
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '');
}

export function getThemeLabel(themeId) {
    const found = THEME_DEFINITIONS.find((theme) => theme.id === themeId);
    return found ? found.label : FALLBACK_THEME.label;
}

export function classifyCardTheme(card) {
    const haystack = [
        card?.en,
        card?.cz,
        ...(Array.isArray(card?.meanings) ? card.meanings : []),
        ...(Array.isArray(card?.tags) ? card.tags : []),
        card?.context,
        card?.contextCz,
    ]
        .map(normalizeText)
        .join(' ');

    let bestMatch = null;
    let bestScore = 0;

    for (const theme of THEME_DEFINITIONS) {
        if (!theme.keywords || theme.keywords.length === 0) {
            continue;
        }

        let matchCount = 0;
        for (const keyword of theme.keywords) {
            const normalizedKeyword = normalizeText(keyword);
            if (haystack.includes(normalizedKeyword)) {
                matchCount++;
            }
        }

        if (matchCount > bestScore) {
            bestScore = matchCount;
            bestMatch = theme.id;
        }
    }

    if (bestMatch && bestScore > 0) {
        return bestMatch;
    }

    return FALLBACK_THEME.id;
}

export function getCardThemeInfo(card, allCards) {
    const themeId = classifyCardTheme(card);
    const themeLabel = getThemeLabel(themeId);
    const themeCards = allCards.filter(c => classifyCardTheme(c) === themeId);
    return { themeId, themeLabel, count: themeCards.length };
}

